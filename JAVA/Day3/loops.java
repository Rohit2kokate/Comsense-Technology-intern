public class loops{
	public static void main(String args[]){
		int i=1,n=5;
		System.out.println("USING WHILE LOOPS");
		while(i<11){
			System.out.println(n+" * "+i+" ="+(n*i));
			i++;
		}
		int m=6;
		System.out.println("USING FOR LOOPS");
		for(int j=1;j<10;j++){
			System.out.println(m+" * "+j+" ="+(m*j));
		}
	}
}